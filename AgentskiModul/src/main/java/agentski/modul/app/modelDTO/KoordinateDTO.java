package agentski.modul.app.modelDTO;

public class KoordinateDTO {
	private Long id;
	private float sirina;
	private float duzina;
	
	public KoordinateDTO() {
		
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public float getSirina() {
		return sirina;
	}

	public void setSirina(float sirina) {
		this.sirina = sirina;
	}

	public float getDuzina() {
		return duzina;
	}

	public void setDuzina(float duzina) {
		this.duzina = duzina;
	}
	
}
